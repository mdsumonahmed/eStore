package com.graphai.estore;

public class ButtonSilder {
    private int icon;
    private String icon_name;

    public ButtonSilder(int icon, String icon_name) {
        this.icon = icon;
        this.icon_name = icon_name;
    }

    public int getIcon() {
        return icon;
    }

    public String getIcon_name() {
        return icon_name;
    }
}
